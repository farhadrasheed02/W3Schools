var = 10
var1 = 3
# Arithmetic Operators
print("Addition:", var + var1)  # Addition
print("Subtraction:", var1 - var)  # Subtraction
print("Multiplication:", var * var1)  # Multiplication
print("Division:", var / var1)  # Division
print("Floor Division:", var // var1)  # Floor Division-- provide how many times we have to divide var by var1 to get the whole number
print("Modulus:", var % var1)  # Modulus --provide the remainder of the division of var by var1
print("Exponentiation:", var ** var1)  # Exponentiation

print(type(var/var1))
print(type(var//var1))
print(type(var%var1))